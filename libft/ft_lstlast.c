#include "../includes/libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*tmp;

	tmp = lst;
	if (!lst)
		return (NULL);
	while (lst)
	{
		lst = lst->next;
		if (lst != NULL)
			tmp = lst;
	}
	return (tmp);
}
